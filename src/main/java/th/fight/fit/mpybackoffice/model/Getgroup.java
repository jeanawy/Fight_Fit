public class Getgroup {

    private String id;
    private String group;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    @Override
    public String toString() {
        return "Getgroup{" + "id=" + id + ", group=" + group + '}';
    }

}
